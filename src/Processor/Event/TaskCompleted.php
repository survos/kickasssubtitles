namespace KickAssSubtitles\Processor\Event;

/**
 * Class TaskCompleted.
 */
class TaskCompleted
{
    /**
     * @var string
     */
    protected $identifier;

    public function __construct(string $identifier)
    {
        $this->identifier = $identifier;
    }

    public function getIdentifier(): string
    {
        return $this->identifier;
    }
}
